/**
 * Grid
 *
 * Represents a grid on which cars can move.
 */
public class Grid {

    int width;
    int height;

    /**
     * Creates a new grid for cars to move on.
     *
     * @param width Width of the grid. 
     * @param height Height of the grid. 
     */
    public Grid(int width, int height) {
        this.width = width;
        this.height = height;
    }
}
